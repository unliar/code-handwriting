var NumArray = function(nums) {
  this.dp = [];
  let total = 0;
  for (let i of nums) {
    total = total + i;
    this.dp.push(total);
  }
};

/**
 * @param {number} left
 * @param {number} right
 * @return {number}
 */
NumArray.prototype.sumRange = function(left, right) {
  return this.dp[right] - (this.dp[left - 1] || 0);
};

